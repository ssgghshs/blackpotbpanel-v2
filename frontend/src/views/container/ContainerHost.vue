// 格式化日期
const formatDate = (dateString) => {
  if (!dateString) return '';
  const date = new Date(dateString);
  return date.toLocaleString();
};

// 获取状态颜色
const getStatusColor = (status) => {
  switch (status) {
    case 'online': return 'green';
    case 'offline': return 'red';
    case 'checking': return 'blue';
    case 'unknown': return 'gray';
    default: return 'gray';
  }
};

// 获取状态文本
const getStatusText = (status) => {
  switch (status) {
    case 'online': return t.value('online');
    case 'offline': return t.value('offline');
    case 'checking': return t.value('checking');
    case 'unknown': return t.value('unknown');
    default: return t.value('unknown');
  }
};

// 获取单个节点的状态（带超时控制）
const fetchNodeStatus = async (nodeId, timeout = 3000) => {
  try {
    const timeoutPromise = new Promise((_, reject) => {
      setTimeout(() => reject(new Error('timeout')), timeout);
    });
    
    const statusPromise = getContainerNodeStatus(nodeId);
    const statusResponse = await Promise.race([statusPromise, timeoutPromise]);
    
    return statusResponse.is_online ? 'online' : 'offline';
  } catch (error) {
    if (error.message === 'timeout') {
      console.warn(`节点 ${nodeId} 状态检查超时`);
    } else {
      console.error(`获取节点 ${nodeId} 状态失败:`, error);
    }
    return 'offline';
  }
};

// 获取容器节点列表
const fetchContainerNodes = async (page = 1) => {
  try {
    loading.value = true;
    const response = await getContainerNodes({
      skip: (page - 1) * pagination.pageSize,
      limit: pagination.pageSize
    });
    
    console.log('容器节点API响应:', response);
    
    // 根据实际API响应结构调整
    let nodes = [];
    let total = 0;
    
    if (response && response.items && Array.isArray(response.items)) {
      // 如果响应包含items字段
      nodes = response.items;
      total = response.total || response.items.length;
    } else if (response && Array.isArray(response)) {
      // 如果响应直接是数组
      nodes = response;
      total = response.length;
    } else {
      nodes = [];
      total = 0;
    }
    
    // 先显示节点列表，状态设为检查中
    const nodesWithPendingStatus = nodes.map(node => ({
      ...node,
      status: 'checking'
    }));
    
    containerNodes.value = nodesWithPendingStatus;
    pagination.total = total;
    loading.value = false; // 先结束加载状态
    
    // 异步获取每个节点的实时状态
    nodes.forEach(async (node, index) => {
      try {
        const status = await fetchNodeStatus(node.id);
        // 更新对应节点的状态
        if (containerNodes.value[index] && containerNodes.value[index].id === node.id) {
          containerNodes.value[index].status = status;
        }
      } catch (error) {
        console.error(`处理节点 ${node.id} 时出错:`, error);
        if (containerNodes.value[index] && containerNodes.value[index].id === node.id) {
          containerNodes.value[index].status = 'unknown';
        }
      }
    });

  } catch (error) {
    console.error('获取容器节点列表失败:', error);
    Message.error(t.value('getContainerNodesFailed'));
    containerNodes.value = [];
    pagination.total = 0;
  } finally {
    loading.value = false;
  }
};

// 处理分页变化
const handlePageChange = (page) => {
  pagination.current = page;
  fetchContainerNodes(page);
};

// 处理分页大小变化
const handlePageSizeChange = (pageSize) => {
  pagination.pageSize = pageSize;
  pagination.current = 1; // 重置到第一页
  fetchContainerNodes(1);
};

// 定时刷新节点状态
const refreshNodeStatuses = () => {
  containerNodes.value.forEach(async (node) => {
    try {
      const status = await fetchNodeStatus(node.id);
      // 更新节点状态
      node.status = status;
    } catch (error) {
      console.error(`刷新节点 ${node.id} 状态失败:`, error);
    }
  });
};

// 抽屉相关状态
const drawerVisible = ref(false);
const isEditMode = ref(false);
const submitting = ref(false);
const nodeFormRef = ref(null);
const editingNodeId = ref(null);

// 表单数据
const nodeForm = reactive({
  name: '',
  description: '',
  identifier: '',
  endpoint_type: 'unix_socket',
  endpoint_url: 'unix:///var/run/docker.sock',
  compose_path: '',
  use_tls: false,
  tls_cert: '',
  tls_key: '',
  tls_ca_cert: ''
});

// 端点路径（不包含协议前缀）
const endpointPath = ref('/var/run/docker.sock');

// 标志：是否正在编辑模式下设置数据
const isSettingEditData = ref(false);

// 计算属性：端点前缀

// 计算属性：占位符文本
const endpointPlaceholder = computed(() => {
  return nodeForm.endpoint_type === 'unix_socket' 
    ? t.value('unixSocketPlaceholder') 
    : t.value('tcpPlaceholder');
});

// 监听节点名称变化，自动更新identifier
watch(() => nodeForm.name, (newName) => {
  // 只有在非编辑模式或者编辑的不是localhost节点时才自动更新
  if (!isEditMode.value || (isEditMode.value && nodeForm.identifier !== 'localhost')) {
    if (newName) {
      // 检查是否包含中文字符
      if (/[\u4e00-\u9fa5]/.test(newName)) {
        // 包含中文，生成随机标识符
        nodeForm.identifier = generateIdentifier(newName);
      } else {
        // 不包含中文，直接使用，移除特殊字符
        nodeForm.identifier = newName.toLowerCase().replace(/[^a-zA-Z0-9]/g, '_');
      }
    } else {
      nodeForm.identifier = '';
    }
  }
});

// 监听identifier变化，自动更新compose_path
watch(() => nodeForm.identifier, (newIdentifier) => {
  if (newIdentifier) {
    nodeForm.compose_path = `/opt/blackpotbpanel-v2/server/composeapp/compose-${newIdentifier}`;
  } else {
    nodeForm.compose_path = '';
  }
});

// 监听连接类型变化，自动切换默认路径
watch(() => nodeForm.endpoint_type, (newType) => {
  // 如果正在设置编辑数据，跳过自动切换
  if (isSettingEditData.value) {
    return;
  }
  
  if (newType === 'unix_socket') {
    endpointPath.value = '/var/run/docker.sock';
    // Unix Socket 不需要 TLS，重置 TLS 配置
    nodeForm.use_tls = false;
    nodeForm.tls_cert = '';
    nodeForm.tls_key = '';
    nodeForm.tls_ca_cert = '';
  } else if (newType === 'tcp') {
    endpointPath.value = '';
  }
  updateEndpointUrl();
});

// 监听端点路径变化，更新完整URL
watch(endpointPath, () => {
  // 如果正在设置编辑数据，跳过自动更新
  if (isSettingEditData.value) {
    return;
  }
  updateEndpointUrl();
});

// 更新完整的端点URL
const updateEndpointUrl = () => {
  const prefix = nodeForm.endpoint_type === 'unix_socket' ? 'unix://' : 'tcp://';
  nodeForm.endpoint_url = prefix + endpointPath.value;
};

// 从完整URL中提取路径部分
const extractPathFromUrl = (url) => {
  if (!url) return '';
  
  if (url.startsWith('unix://')) {
    return url.substring(7); // 移除 'unix://' 前缀
  } else if (url.startsWith('tcp://')) {
    return url.substring(6); // 移除 'tcp://' 前缀
  }
  
  return url;
};

