    submitting.value = true;
    
    // 构建请求数据 - 根据后端实际数据结构调整
    const formData = {
      name: nodeForm.name,
      description: nodeForm.description,
      identifier: nodeForm.identifier,
      endpoint_type: nodeForm.endpoint_type,
      endpoint_url: nodeForm.endpoint_url,
      compose_path: nodeForm.compose_path,
      use_tls: nodeForm.endpoint_type === 'tcp' ? nodeForm.use_tls : false,
      client_cert: nodeForm.endpoint_type === 'tcp' ? nodeForm.tls_cert : '',
      client_key: nodeForm.endpoint_type === 'tcp' ? nodeForm.tls_key : '',
      ca_cert: nodeForm.endpoint_type === 'tcp' ? nodeForm.tls_ca_cert : '',
      connection_config: {
        base_url: nodeForm.endpoint_url
      }
    };
    
    // 添加调试日志，查看提交的数据
    console.log('提交的表单数据:', JSON.stringify(formData, null, 2));
    
    if (isEditMode.value && editingNodeId.value) {
      // 更新节点
      await updateContainerNode(editingNodeId.value, formData);
      Message.success(t.value('updateSuccess'));
    } else {
      // 创建节点
      await createContainerNode(formData);
      Message.success(t.value('createSuccess'));
    }
    
    // 关闭抽屉
    closeDrawer();
    
    // 重新加载节点列表
    fetchContainerNodes(pagination.current);
    
  } catch (error) {
    console.error('提交表单失败:', error);
    Message.error(error.message || t.value('operationFailed'));
  } finally {
    submitting.value = false;
  }
};

// 处理文件上传
const handleFileUpload = (fieldName) => {
  const input = document.createElement('input');
  input.type = 'file';
  input.accept = '.pem,.crt,.key,.cert';
  
  input.onchange = (event) => {
    const file = event.target.files[0];
    if (file) {
      const reader = new FileReader();
      reader.onload = (e) => {
        const content = e.target.result;
        nodeForm[fieldName] = content;
        Message.success(t.value('fileUploadSuccess'));
      };
      reader.onerror = () => {
        Message.error(t.value('fileUploadFailed'));
      };
      reader.readAsText(file);
    }
  };
  
  input.click();
};

