  nodeForm.endpoint_url = originalEndpointUrl;
  
  // 从记录中直接提取TLS配置（从顶级字段）
  nodeForm.use_tls = record.use_tls || false;
  // 将后端的client_cert映射到前端的tls_cert
  nodeForm.tls_cert = record.client_cert || '';
  // 将后端的client_key映射到前端的tls_key
  nodeForm.tls_key = record.client_key || '';
  // 将后端的ca_cert映射到前端的tls_ca_cert
  nodeForm.tls_ca_cert = record.ca_cert || '';
  
  // 添加调试日志，查看读取的数据
  console.log('编辑模式下读取的TLS配置:', {
    use_tls: nodeForm.use_tls,
    tls_cert: nodeForm.tls_cert,
    tls_key: nodeForm.tls_key,
    tls_ca_cert: nodeForm.tls_ca_cert
  });
  
  // 等待 Vue 更新周期完成后再设置端点路径
  await nextTick();
  
  // 提取端点路径（去掉协议前缀）
  endpointPath.value = extractPathFromUrl(originalEndpointUrl);
  
  // 重置标志
  isSettingEditData.value = false;
  
  drawerVisible.value = true;
};

