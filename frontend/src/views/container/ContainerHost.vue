const showDeleteModal = (record) => {
  // 前端添加删除限制：当identifier为localhost时不允许删除
  if (record.identifier === 'localhost') {
    Message.warning(t.value('cannotDeleteLocalhostNode'));
    return;
  }
  nodeToDelete.value = record;
  deleteModalVisible.value = true;
};

// 取消删除
const cancelDelete = () => {
  deleteModalVisible.value = false;
  nodeToDelete.value = null;
};

// 确认删除
const confirmDelete = async () => {
  if (!nodeToDelete.value) {
    cancelDelete();
    return;
  }
  
  // 再次确认，防止绕过前端限制
  if (nodeToDelete.value.identifier === 'localhost') {
    Message.warning(t.value('cannotDeleteLocalhostNode'));
    cancelDelete();
    return;
  }
  
  try {
    await deleteContainerNode(nodeToDelete.value.id);
    Message.success(t.value('deleteSuccess'));
    
    // 重新加载节点列表
    fetchContainerNodes(pagination.current);
  } catch (error) {
    console.error('删除节点失败:', error);
    // 如果是因为权限错误（后端限制），显示更具体的错误信息
    if (error.response && error.response.status === 403) {
      Message.error(t.value('cannotDeleteLocalhostNode'));
    } else {
      Message.error(error.message || t.value('operationFailed'));
    }
  } finally {
    cancelDelete();
  }
};

